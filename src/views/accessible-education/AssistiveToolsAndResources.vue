<h2>Screen Readers</h2>
<p>Screen Readers are an essential piece of software for users with visual impairments. It processes text on a screen into an accessible format, usually audio. These can range in sophistication and features. The following list features free screen readers available for download.</p>

<ul>
<li><a href="http://www.nvda-project.org/">NVDA (Windows)</a>
<br>NVDA has been designed by a blind software engineering graduate, James Teh, for use with Windows computers. This free and open source screen reader has a synthetic voice that reads whatever the cursor hovers over, and can be used directly from a USB stick, making it ideal for students.
</li>

<li><a href="https://www.satogo.com/en/">Serotek System Access (Windows)</a>
<br>This downloadable and complete screen reader can be used even outside your browser, thus making it one of the quickest ways of getting a screen reader up and running on your system. Serotek offers extended versions for a fee, although it is much cheaper than other screen readers.
</li>

<li><a href="http://www.apple.com/accessibility/voiceover/">Apple VoiceOver</a>
<br>Apple VoiceOver includes options for maginification, keyboard control and verbal descriptions in English to describe what is happening on screen. It reads aloud file content as well as web pages, E-mail messages and word processing files whilst providing a relatively accurate narrative of the user’s workspace. This covers a wide array of keyboard commands that enable user navigation of the Mac OS X interface.
</li>

<li><a href="http://projects.gnome.org/orca/">ORCA (Linux)</a>
<br>ORCA is a Linux based screen reader which has also been evolving for the past number of years. Although it is not the sole Linux-based screen reader, ORCA is definitely the most popular. Recently it has been included with the Ubuntu installation CD, and with a couple of initial key presses it allows blind people to have audible interaction during the installation process.
</li>

<li><a href="http://mielke.cc/brltty/">BRLTTY (Linux)</a>
<br>BRLTTY is a background process (daemon) which provides access to the Linux/Unix console (when in text mode) for a blind person using a refreshable braille display. It drives the braille display, and provides complete screen review functionality. Some speech capability has also been incorporated.
</li>

<li><a href="http://emacspeak.sourceforge.net/">Emacspeak (Linux)</a>
<br>Emacspeak is a free speech interface and that allows visually impaired users to interact independently and efficiently with the computer. Its technology enables it to produce rich aural representation of electronic information. Emacspeak offers an audible interface of the different aspects of the Internet such as browsing and messaging as well as local and remote information via a consistent and well-integrated user interface.
</li>

<li><a href="http://webinsight.cs.washington.edu/wa/content.php">WebAnywhere (All OSs, Web browsers)</a>
<br>WebAnywhere is a web-based screen reader. It requires no special software to be installed on the client machine and, therefore, enables blind people to access the web from any computer they happen to have access to that has a sound card.
</li>

<li><a href="https://chrome.google.com/webstore/detail/screen-reader/kgejglhpjiefppelpmljglcjbhoiplfn?hl=en">Screen Reader (Google Chrome Extension)</a>
<br>The Chrome Screen reader extension is a showcase of a fully featured screen reader for the web built using only web technologies such as html and javascript.
</li>

<li><a href="https://addons.mozilla.org/en-US/firefox/addon/pericles/">Pericles (Firefox Extension)</a>
<br>Web-page TTS (text to speech) screen reader, that does not require cutting and pasting sentences, paragraphs or entire page sections.
</li>


</ul>


<h2>Text-to-Speech Generators</h2>
<p>A text-to-speech (TTS) system converts normal language text into speech. An intelligible text-to-speech program allows people with visual impairments or reading disabilities to listen to written words on a home computer. Many computer operating systems have included speech synthesizers since the early 1990s.</p>

<ul>
<li><a href="http://www.fromtexttospeech.com/">From Text To Speech</a>
<br>A very simple web interface for copy and paste text to speech functions online. There are several languages, voices, and speeds available to select.
</li>

<li><a href="https://freetts.sourceforge.io/">FreeTTS</a>
<br>A free, open source, cross-platform TTS written in java. FreeTTS supports end-of-speech markers, and is based on the Flite TTS engine.
</li>

<li><a href="https://ttsreader.com/">TTSREADER</a>
<br>A web-app that reads out loud any text (typed-in or files) with natural sounding voices. Free, fun and unlimited.
</li>

<li><a href="https://www.naturalreaders.com/online/">Natural Reader</a>
<br>Natural Reader is a professional text to speech program that converts any written text into spoken words. The paid versions of Natural Reader have many more features.
</li>

<li><a href="https://www.ispeech.org/text.to.speech">iSpeech</a>
<br>iSpeech text to speech program is free to use, offers 28 languages and is available for web and mobile use. For Developers, iSpeech offers free mobile and web SDKs.
</li>

<li><a href="https://inforobo.com/text-to-speech-online/">inforobo Free Online Text to Speech Reader</a>
<br>This is a text to speech web application that reads any typed or pasted text in the text box with natural sounding voices. This program is absolutely free to use (unlimited times) and it runs both online and offline. It is very easy to use and no download or login is required.
</li>


</ul>

<h2>Graphic Organizers</h2>
<p>A graphic organizer, also known as knowledge map, concept map, story map, cognitive organizer, advance organizer, or concept diagram, is a communication tool that uses visual symbols to express knowledge, concepts, thoughts, or ideas, and the relationships between them. These tools are useful for all users, and can be used to create notes, concept maps, and outlines, as well as beautiful infographics and multimedia presentations.</p>

<ul>
<li><a href="https://www.canva.com/">Canva</a>
<br>Canva is a graphic-design tool website, founded in 2012. It uses a drag-and-drop format and provides access to over a million photographs, graphics, and fonts. It is used by non-designers as well as professionals.
</li>

<li><a href="https://piktochart.com/">Piktochart</a>
<br>Piktochart is a web-based infographic application which allows users without intensive experience as graphic designers to easily create infographics and visuals using themed templates.
</li>

<li><a href="https://prezi.com/">Prezi</a>
<br>Prezi is a presentation software that uses motion, zoom, and spatial relationships to bring your ideas to life and make you a great presenter.
</li>

<li><a href="https://www.mindmeister.com/">MindMeister</a>
<br>A beautiful looking mind mapping site with the ability to embed into a site or blog.
</li>

<li><a href="https://mind42.com/">Mind42</a>
<br>Mind42 is a free, fast, and simple online mind mapping software.
</li>

<li><a href="https://www.mindmup.com/">MindMup</a>
<br>A easy to use site for creating brainstorms or mind maps.
</li>

<li><a href="https://www.mindomo.com/">Mindomo</a>
<br>Collaborative mind mapping, concept mapping, outlining and more for teachers and students.
</li>

<li><a href="http://popplet.com/">Popplet</a>
<br>Popplet is a tool for the iPad and web to capture and organize your ideas. In the classroom and at home, students use Popplet for learning. In the office or on the go, professionals use Popplet to generate ideas and plan projects. Used to brainstorm or jot down notes, Popplet helps professionals organize their thoughts and generate their next big idea.
</li>

<li><a href="http://www.wisemapping.com/">Wise Mapping</a>
<br>Free online, and open source mind mapping editor for individuals and business.
</li>


</ul>


<h2>Online Proofreaders</h2>
<p>Another set of tools that is useful for all users, Online Proofreading is the process of carefully reviewing a text for errors, especially surface errors, in the online environment.</p>

<ul>
<li><a href="https://www.grammarly.com/proofreading">Grammarly</a>
<br>Grammarly’s online proofreader automatically detects grammar, spelling, punctuation, word choice and style mistakes in your writing.
</li>

<li><a href="https://www.slickwrite.com/#!home">Slick Write</a>
<br>Slick Write is a powerful, free application that makes it easy to check your writing for grammar errors, potential stylistic mistakes, and other features of interest. Whether you're a blogger, novelist, SEO professional, or student writing an essay for school, Slick Write can help take your writing to the next level.
</li>

<li><a href="http://www.paperrater.com/">PaperRater.com</a>
<br>PaperRater.com is a free resource that utilizes Artificial Intelligence to improve writing. They believe that accessibility to quality educational tools is the fundamental component of their mission. They offer a premium subscription for users seeking more advanced features.
</li>

<li><a href="https://www.gingersoftware.com/proofreading">Ginger Online Proofreading Service</a>
<br>Ginger’s proofreader consists of a spell checker and a grammar checker that cover a vast range of errors, from simple typos, through severe misspellings and to sophisticated grammar mistakes and wrong usage of words. It successfully spots and corrects mistakes that traditional proofreading tools leave overlooked.
</li>

<li><a href="https://editor.typely.com/">Typely</a>
<br>A fairly new online service that takes a unique and thoughtful approach to proofreading services.
</li>


</ul>
