<h2>Frequently Asked Questions</h2>

<p><strong>What do I do if a student discloses a disability or asks about accommodations/Disability Services?</strong></p>
<p>Tell the student to reach out to Disability Services as soon as possible by emailing <a href="mailto:ada@csuglobal.edu">ada@csuglobal.edu</a> or by calling <a href="tel:1-720-279-0650">720-279-0650</a>. Once the student connects with Disability Services, our office can assist them through the process of registering with our office and requesting/accepting academic accommodations. You can tell students that we strive to respond to inquiries within 1-2 business days. Even if a student does not complete the process of accepting academic accommodations, Disability Services can still provide information about resources that may be helpful for the student as they progress through their coursework.</p>
<p>&nbsp;</p>

<p><strong>I received a “Faculty Notification Letter” from a student in my course. What does this mean and what do I need to do?</strong></p>
<p>Faculty Notification Letters list information about a student’s approved academic accommodations. The accommodation information on a Faculty Notification Letter has been reviewed and approved by Disability Services. Disability Services will provide a letter to the student at their request. The student then sends the letter to their instructor. Once an instructor receives a Faculty Notification Letter from a student, the student’s accommodations are effective for the remainder of the term.</p> 

<p>Accommodations become effective in a course/term the date the letter is delivered to the instructor and are not retroactive. We recommend that students request their Faculty Notification Letter prior to the start of a course and notify their instructor within the first week of the term. However, this process can be completed at any point during an active term. </p>

<p>It is incredibly important that faculty honor the approved accommodations listed on the Faculty Notification Letter. Please review the information thoroughly and reach out to Disability Services at ada@csuglobal.edu if there are any questions or concerns.</p>
<p>&nbsp;</p>

<p><strong>As a CSU Global employee, I am interested in learning more information about potential workplace accommodations - what are my next steps?</strong></p>
<p>For information about workplace accommodations as a CSU Global employee, please reach out to <a href="hr@csuglobal.edu">hr@csuglobal.edu</a>. Our Human Resources Office can assist you with this process.</p> 
