<h2>Job Search Resources</h2>

<p><a href="https://www.gettinghired.com/">GettingHired</a><br>
This is a recruitment resource which is committed to helping inclusive employers connect with and hire individuals and veterans with disabilities. Job seekers have access to a career portal with connections to a variety of employment opportunities.</p>

<p><a href="https://abilityjobs.com/">abilityJOBS</a><br>
Established in 1995 and dedicated to the employment of individuals with disabilities. Job seekers can search for jobs in the abilityJOBS database, set up job alerts and also add their resume to the abilityJOBS resume bank (a search tool for employers).</p>

<p><a href="https://www.usajobs.gov/Help/working-in-government/unique-hiring-paths/individuals-with-disabilities/">USAJOBS</a><br>
Connects job seekers with Federal employment opportunities. Every person employed by the Federal Government is hired using a hiring authority. This specific site has information about the Schedule A Hiring Authority which gives Federal agencies an optional, potentially expedited process to hire individuals with disabilities.</p>

<p><a href="https://jobs.localjobnetwork.com/disability">Disability Job Exchange</a><br>
This is a job search website which focuses on the employment of individuals with disabilities. The Disability Job Exchange team also provides resume and career advice to job seekers.</p>

<p><a href="https://www.wrp.gov/wrp">Workforce Recruitment Program</a><br>
A yearly recruitment program which connects federal and private sector employers to current college students or recent graduates for summer or permanent job opportunities. CSU-Global participates in this program which begins around September of every year. If you are interested in participating in the WRP, please email <a href="mailto:ada@csuglobal.edu">ada@csuglobal.edu</a>.</p>

    
