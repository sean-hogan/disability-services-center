<p>If the grievance is with the accommodation process or with Disability and Accessibility Services, the student is encouraged to resolve the concerns directly with Disability and Accessibility Services within ten business days of the initial complaint. If the initial complaint is made after ten days, the complaint/grievance will be deemed untimely.</p>

<p>If an informal resolution cannot be reached, students have the option of filing a formal grievance with the Grievance Committee. This must be received, in writing, within ten days of the date the informal grievance process ends. To pursue a formal investigation, students should be prepared to provide the name of the staff member, a detailed summary of the issue or concern, an account of previous communication that was attempted to resolve the concern, and a brief statement describing the desired outcome. Students can request a Grievance Form from their Student Success Counselor, which must be submitted with supporting documentation to substantiate the claim.</p>

<p>The grievance is reviewed by the Grievance Committee which is a subcommittee of the Student Affairs committee. The Grievance Committee will review the grievance and communicate the decision that is made to all parties involved within five days.</p>

<p>Any decision of the Grievance Committee may be appealed by the student if there is evidence that a significant impropriety in the review process occurred. The appeal must be in writing, specify in detail the alleged procedural impropriety, and must be filed in the Office of the Provost within ten business days of the date of receipt of the Grievance Committee's decision. The Provost shall review the appeal and the Grievance Committee records and issue a decision within ten days. The decision of the Provost is final.</p>

<p>Students may also contact the Office for Civil Rights to file a complaint.</p>
