<h2>General Information</h2>

<ul>
<li><a href="https://medlineplus.gov/disabilities.html">Disabilities Information from the National Library of Medicine</a>
<br>A number of resources and articles that span the field from the U.S. National Library of Medicine.
</li>

<li><a href="http://www.parentcenterhub.org/">Center for Parent Information &amp; Resources (CPIR)</a>
<br>A central “Hub” of information and products created for the network of Parent Centers serving families of children with disabilities.
</li>

<li><a href="https://www.loc.gov/nls/">National Library Service for the Blind and Physically Handicapped</a>
<br>National Library Service (NLS) is a free braille and talking book library service for people with temporary or permanent low vision, blindness, or a physical disability that prevents them from reading or holding the printed page. Through a national network of cooperating libraries, NLS circulates books and magazines in braille or audio formats, delivered by postage-free mail or instantly downloadable.
</li>

</ul>


<h2>Employment</h2>


<ul>
<li><a href="https://www.askearn.org/state-vocational-rehabilitation-agencies/">Department of Vocational Rehabilitation (VR)</a>
<br>Vocational Rehabilitation is a nationwide federal-state program for assisting eligible people with disabilities to define a suitable employment goal and become employed. Each state capital has a central VR agency, and there are local offices in most states. VR provides medical, therapeutic, counseling, education, training, and other services needed to prepare people with disabilities for work. VR is an excellent place for a youth or adult with a disability to begin exploring available training and support service options.
</li>

<li><a href="https://askjan.org/">Job Accommodation Network (JAN)</a>
<br>The Job Accommodation Network (JAN) is the leading source of free, expert, and confidential guidance on workplace accommodations and disability employment issues. Working toward practical solutions that benefit both employer and employee, JAN helps people with disabilities enhance their employability, and shows employers how to capitalize on the value and talent that people with disabilities add to the workplace.
</li>

<li><a href="https://adata.org/">ADA National Network</a>
<br>The ADA National Network provides information, guidance and training on the Americans with Disabilities Act (ADA), tailored to meet the needs of business, government and individuals at local, regional and national levels. The ADA National Network consists of an ADA Knowledge Translation Center and 10 regional centers located throughout the United States that provides personalized, local assistance to ensure that the ADA is implemented wherever possible. This is not an enforcement or regulatory agency, but a helpful resource supporting the ADA’s mission to “make it possible for everyone with a disability to live a life of freedom and equality.”
</li>

<li><a href="http://careeronestop.org/">Career One-Stop</a>
<br>This website is a publicly funded resource for job-seekers (including those with disabilities) and businesses. Job-seekers can search for jobs—from entry level to technical to professional to CEO—locate public workforce services in their area, explore alternative career paths, compare salary data for different occupations, learn which careers are hot, get resume writing tips and job interview strategies, and much more. Employers can identify job-ready workers with the right skills.
</li>

<li><a href="http://abilityjobs.com/">JobAccess and ABILITYJobs</a>
<br>The goal of ABILITYJobs and JobAccess is to enable people with disabilities to enhance their professional lives by providing a dedicated system for finding employment. By posting job opportunities, or searching resumes, employers can find qualified persons with disabilities as well as demonstrate their affirmative action and open door policies.
</li>

<li><a href="http://www.onestops.info/">National Center on Workforce and Disability/Adult (NCWD)</a>
<br>The National Center on Workforce and Disability/Adult (NCWD) provides training, technical assistance, policy analysis, and information to improve access for all in the workforce development system. Areas of expertise include: accommodations and assistive technology, relationships with employers, helping clients with disabilities find jobs, and advising employers as to how to provide job-related supports.
</li>


<li><a href="http://www.sourceamerica.org/">SourceAmerica</a>
<br>SourceAmerica is a national nonprofit agency whose mission is to create employment opportunities for people with significant disabilities by securing federal contracts through the AbilityOne Program for its network of community-based, nonprofit agencies. The AbilityOne Program is the largest single source of employment for people who are blind or have other significant disabilities in the United States.
</li>

<li><a href="http://www.dol.gov/odep">Office of Disability Employment Policy</a>
<br>The Office of Disability Employment Policy (ODEP) provides national leadership on disability employment policy by developing and influencing the use of evidence-based disability employment policies and practices, building collaborative partnerships, and delivering authoritative and credible data on employment of people with disabilities. Find a wealth of employment-related information on ODEP’s website.
</li>

<li><a href="http://askearn.org/"> EARN | The Employer Assistance and Resource Network</a>
<br>The Employer Assistance &amp; Resource Network (EARN) provides federal and private employers with free consulting services and resources to support the recruitment, hiring, and retention of people with disabilities. EARN connects employers with national networks of available job seekers and also provides high quality up-to-date online information and technical assistance to promote the inclusion of employees with disabilities in the workplace. Job-seekers can use EARN’s online tools and resources to find employment opportunities, and be connected with local employment service providers.
</li>

<li><a href="http://www.ssa.gov/work">Social Security Administration (SSA)</a>
<br>The Social Security Administration’s Work Site provides clarity on matters affecting the employment of Social Security beneficiaries with disabilities. Its "Ticket to Work Program" provides most beneficiaries with more choices for receiving employment services. Under this program SSA issues tickets to eligible beneficiaries who, in turn, may choose to assign those tickets to an Employment Network (EN) of their choice to obtain employment services, vocational rehabilitation services, or other support services necessary to achieve a vocational (work) goal. The EN, if they accept the ticket, will coordinate and provide appropriate services to help the beneficiary find and maintain employment.
</li>

<li><a href="http://worksupport.com/index.cfm">Work Support</a>
<br>Work Support is a web portal that highlights the funded projects of Virginia Commonwealth University on many topics related to the employment of individuals with disabilities. This includes the RRTC on Employment of People with Physical Disabilities, Autism Center for Excellence, School 2 Work, and the Center on Transition to Employment for Youth with Disabilities.
</li>

<li><a href="http://www.access-board.gov/">Access Board</a>
<br>The Access Board is an independent Federal agency devoted to accessibility for people with disabilities. Created in 1973 to ensure access to federally funded facilities, the Board is now a leading source of information on accessible design. If you are concerned about access to a facility that may have been federally funded, you can file a complaint about it with the Access Board under the Architectural Barriers Act (ABA).
</li>

<li><a href="http://www.eeoc.gov/">Equal Employment Opportunity Commission (EEOC)</a>
<br>The EEOC is a government agency that handles discrimination complaints about employment based on age, sex, race, ethnicity, and disability. The 800 number will connect callers with their local EEOC office, which can discuss complaints.
</li>


</ul>

<h2>Postsecondary Education</h2>


<ul>
<li><a href="http://www.ahead.org/">Association on Higher Education and Disability (AHEAD)</a>
<br>AHEAD is an international, multicultural organization of professionals committed to full participation in higher education for persons with disabilities. AHEAD offers numerous training programs, workshops, publications, and conferences to promote this mission.
</li>

<li><a href="http://www.heath.gwu.edu/">HEATH Resource Center</a>
<br>HEATH is the online clearinghouse on postsecondary education for individuals with disabilities. Come here if you’re looking for information about educational support services, policies, procedures, adaptations, and opportunities at American campuses, vocational-technical schools, and other postsecondary training entities for adults with disabilities..
</li>

<li><a href="http://www.learningally.org/default.aspx">Learning Ally</a>
<br>Learning Ally is a national nonprofit service organization that provides educational and professional books in accessible format to people with visual impairments, learning disabilities, and other physical disabilities that prevent them from reading printed material. This includes individuals who are no longer in school but who are using educational books to pursue careers or personal interests. An annual membership costs $119/year (in 2016) and gives members unlimited access to over 80,000 audiobooks and other resources.
</li>

<li><a href="http://study.com/academy/course/index.html">Study.com</a>
<br>Your portal to degree programs, career information, school reviews, and education news.
</li>

<li><a href="https://dcmp.org/">Described and Captioned Media Program</a>
<br>DCMP provides a variety of resources to serve students with sensory disabilities, and holds the standard for described and captioned media. Free memberships are available for qualifying educators and users.
</li>


</ul>

<h2>Recreation</h2>


<ul>
<li><a href="http://www.nps.gov/findapark/index.htm">Department of the Interior</a>
<br>There are 370 parks and 7 regional offices under the National Park Service. A listing of all national parks and facilities, including general information about their accessibility, is available at the link above. However, information on accessibility of park programs, facilities, and services is best acquired directly from the park or area you plan to visit.
</li>

<li><a href="http://www.disabledsportsusa.org/chapters/">Disabled Sports USA</a>
<br>Disabled Sports USA is the nation’s largest organization providing year-round sports and recreation activities to children and adults with physical disabilities. In conjunction with its nationwide network of chapters serving people in all 50 states and Puerto Rico, Disabled Sports USA offers such activities as snow skiing, water skiing, bicycling, white water rafting, horseback riding, mountain climbing, sailing, camping, and track and field. Use the link to identify chapters in your area.
</li>

<li><a href="http://www.miusa.org/">Mobility International USA (MIUSA)</a>
<br>MIUSA is a nonprofit membership organization for persons with disabilities and other interested people. It works to expand opportunities for persons with disabilities to be involved in international educational exchange programs and travel. MIUSA members receive information and referral services in the areas of travel and placement in international work camps and educational exchange programs. MISUA also conducts international leadership training for persons with disabilities. Many publications are also available.
</li>

<li><a href="http://www.ncaonline.org/">National Center on Accessibility</a>
<br>The National Center on Accessibility, which focuses upon making parks, recreation, and tourism accessible to individuals with disabilities, provides information on: access for individuals with disabilities to park and recreation areas and programs; training programs and opportunities; technical assistance for park and recreation professionals; and research and demonstration projects.
</li>

<li><a href="http://www.nchpad.org/">National Center on Health, Physical Activity and Disability (NCHPAD)</a>
<br>NCHPAD provides information and resources that can enable people with disabilities to become as physically active as they choose to be. They maintain searchable directories of organizations, programs, and facilities that provide opportunities for accessible physical activity, adaptive equipment vendors, conferences and meetings, and references to journal articles, books, videos and more. Fact sheets on a variety of physical activities for people with disabilities are also available.
</li>

<li><a href="http://www.usarc.org/">United States Adaptive Recreation Center</a>
<br>The USARC works with schools, hospitals, rehabilitation centers, and park and recreation departments to serve children and adults with all types of cognitive or physical disabilities. A variety of summer and winter recreational opportunities are offered. Reservations, by individuals and groups, are required for all USARC programs.
</li>


</ul>

<h2>Independent Living</h2>


<ul>
<li><a href="http://www.abledata.com/">AbleData</a>
