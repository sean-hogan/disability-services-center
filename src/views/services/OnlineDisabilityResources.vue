</li>

<li><a href="http://www.benefits.gov/">Government Benefits</a>
<br>This website, made available by the government helps people find government benefits they may be eligible to receive. Its screening instrument is free and confidential.
</li>

<li><a href="http://www.ncil.org/">National Council on Independent Living (NCIL)</a>
<br>NCIL is the national membership association of local nonprofit corporations known as Centers for Independent Living (CIL).
</li>

<li><a href="http://www.naric.com/">National Rehabilitation Information Center (NARIC)</a>
<br>NARIC serves as an online gateway to an abundance of disability- and rehabilitation-oriented information organized in a variety of formats designed to make it easy for users to find and use. This includes resources for employment, advocacy, benefits and financial assistance, education, technology, and more.
</li>

<li><a href="http://rtcil.org/products">Research and Training Center on Independent Living</a>
<br>The Center’s goal is to develop and disseminate practical techniques that enable people with disabilities to live more independently.
</li>

<li><a href="http://www.socialsecurity.gov/disability/">Social Security Administration (SSA)</a>
<br>The Social Security Administration provides cash benefits (SSI and/or SSDI) to persons with a physical or mental disability that prevents them from working and which is expected to last at least a year or be permanent. Eligibility for SSI or SSDI may mean eligibility for other services, such as Medicaid, food stamps, or other social services. The amount of money and services received varies in each state. The program also includes work incentives that make it possible for individuals to work without an immediate loss of benefits.
</li>


</ul>

<h2>Assistive Technology</h2>


<ul>
<li><a href="http://assistivetech.net/">Assistivetech.net</a>
